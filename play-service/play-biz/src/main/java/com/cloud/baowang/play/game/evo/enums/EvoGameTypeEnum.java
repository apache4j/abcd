    /**
     * 根据 gameType 获取对应的展示名称
     *
     * @param gameType 游戏类型标识
     * @return 对应的展示名称，未找到则返回 null
     */
    public static String getNameByType(String gameType, String lang) {
        for (EvoGameTypeEnum e : values()) {
            if (e.getGameType().equals(gameType)) {
                if (lang.equals(LanguageEnum.ZH_CN.getLang())) {
                    return e.getZhName();
                }
                return e.getEnName();
            }
        }
        return null;
    }
    public static EvoGameTypeEnum ofCode(String code) {
        for (EvoGameTypeEnum e : values()) {
            if (e.getGameType().equals(code)) {
                return e;
            }
        }
        return null;
    }

    public String getGameType() {
        return gameType;
    }

